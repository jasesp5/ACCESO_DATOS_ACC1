    public void printAllUsers() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        for (User user : this.userService.getUsers().values()) {
            model.addRow(new Object[]{user.getUserName(), user.getEmail(), user.getAge(), user.getPostCode()});
        }

    }

