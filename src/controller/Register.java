    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if (this.userService.checkEmail(this.jTextFieldEmail.getText())) {
            this.jLabelEmail.setText("This email already exists");
        } else if (this.userService.checkUsername(this.jTextFieldUsername.getText())) {
            this.jLabelUsername.setText("This username already exists");
        } else if (!this.userService.checkIfTheEmailIsValid(this.jTextFieldEmail.getText())) {
            this.jLabelEmail.setText("This email is no valid");
        } else {
            createUserAndGoToLogin();
            emailSender.createEmail(this.jTextFieldEmail.getText());
            emailSender.sendEmail();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

