    public void createUserAndAddToTheArray() {
        UUID idOne = UUID.randomUUID();
        String email = jTextFieldEmail.getText();
        String userName = jTextFieldUsername.getText();
        String password = String.valueOf(jPasswordField1.getPassword());
        int postCode = Integer.valueOf(jTextField1PostCode.getText());
        int age = Integer.valueOf((String) jComboBox1.getSelectedItem());
        String id = String.valueOf(idOne);
        LogInUser loginUser = new LogInUser();
        this.userService.createUsers(email, userName, password, age, postCode, id, loginUser);

    }

