    private void jButton1LogInActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1LogInActionPerformed
        // TODO add your handling code here:
        String email = this.jTextField1EmailLogin.getText();
        String password = String.valueOf(this.jPasswordField1Login.getPassword());
        if (this.userService.checkEmail(email)) {
            LocalDateTime time = LocalDateTime.now();
           this.userService.checkLasTimeLoginAndSetTheAttempts(email,time);
            checkTheAttemptsAndThePassword(email,password);
        } else {
            this.jLabelErrorLogin.setText("The email does not exist");
        }
    }//GEN-LAST:event_jButton1LogInActionPerformed
    /**
     * MÃ©tood que llama a sumar intentos y luego los escribe 
     * @param email String
     */
    public void plusTheAttemptsAndShowError(String email) {
        this.userService.plusTheAttempts(email);
        LocalDateTime time = LocalDateTime.now();
        this.userService.setLastTimeLogIn(email, time);
        this.userService.writeUsers();
        this.jLabelErrorLogin.setText("The password is not correct please try again");
        jPasswordField1Login.setText("");
    }
    
    /**
     * MÃ©todo que comprueba los intentos y la contraseÃ±a
     * @param email String 
     * @param password String
     */
    public void checkTheAttemptsAndThePassword(String email, String password) {
        if (this.userService.checkAttempts(email)) {
            JOptionPane.showMessageDialog(null, "You have exceeded the maximum amount of times you can login come back after 24 hours");
        } else if (this.userService.checkPassword(email, password)) {
            LocalDateTime time = LocalDateTime.now();
            this.userService.setLastTimeLogIn(email, time);
            this.userService.writeUsers();
            ShowUsers showUSers = new ShowUsers(this.userService);
            showUSers.setVisible(true);
            this.dispose();
        } else {
            plusTheAttemptsAndShowError(email);
        }

    }
